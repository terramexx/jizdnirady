package jizdnirady.jdf;

public class JdfParserException extends Exception {

    public JdfParserException(String message) {
        super(message);
    }

    public JdfParserException(Throwable cause) {
        super(cause);
    }

    public JdfParserException(String message, Throwable cause) {
        super(message, cause);
    }
}
